# # Define a simple sequential model
def create_model():
  modelCNN = tf.keras.models.Sequential()
  modelCNN.add(keras.layers.Conv2D(32, kernel_size=(3, 3), activation='relu', input_shape=(64, 64, 3)))
  modelCNN.add(keras.layers.Conv2D(64, kernel_size=(3, 3), activation='relu'))
  modelCNN.add(keras.layers.MaxPooling2D(4, 4))
  modelCNN.add(keras.layers.Conv2D(64, kernel_size=(3, 3), activation='relu'))
  modelCNN.add(keras.layers.Conv2D(128, kernel_size=(3, 3), activation='relu'))
  modelCNN.add(keras.layers.MaxPooling2D(4, 4))
  modelCNN.add(keras.layers.Flatten())
  modelCNN.add(keras.layers.Dense(128, activation='relu'))
  modelCNN.add(keras.layers.Dense(64, activation='relu'))
  modelCNN.add(keras.layers.Dense(32, activation='relu'))
  modelCNN.add(keras.layers.Dropout(rate=0.5))
  modelCNN.add(keras.layers.Dense(6, activation='softmax'))
  modelCNN.compile(optimizer=keras.optimizers.Adam(0.0001),
                loss=tf.losses.SparseCategoricalCrossentropy(from_logits=True),
                metrics=[tf.metrics.SparseCategoricalAccuracy()])

  return modelCNN

def getLabel(class_index):
    labels = {0: 'buildings', 1: 'forest', 2: 'glacier',  3: 'mountain', 4: 'sea',   5: 'street'}
    return labels[class_index]

